const ul = document.querySelector(".ul")
const choise = document.querySelector(".choise")




function search1(){

    if(choise.value==1){
        savol = xalqaro_savollar
    }

    else if(choise.value==2){
        savol = soliq_huquqi_savollar
    }

    if(inp.value<2) return
    let newLst = []
    for(let sav of savol){
        if(sav.toLocaleLowerCase().includes(inp.value.toLocaleLowerCase())){
            newLst.push(sav)
        }
    }
    render(newLst)
}


function render(lst){

    ul.innerHTML = null

    if(lst.length==0){
        if(choise.value==1){
            sav = xalqaro_savollar
        }
        else if (choise.value==2){
            sav = soliq_huquqi_savollar
        }
    }
    
    for(let sav of lst){
        let li = document.createElement("li")
        li.textContent = sav

        if(choise.value==2){
            li.classList.add("color-change")
        }

        ul.append(li)
    }
}


inp.addEventListener("keyup",()=>{
    search1()
})

